import mongoose from "mongoose";
let Schema = mongoose.Schema;

// Create Schema
let UserSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  register_date: {
    type: Date,
    default: Date.now
  }
});

const User = mongoose.model("user", UserSchema);

export default User;